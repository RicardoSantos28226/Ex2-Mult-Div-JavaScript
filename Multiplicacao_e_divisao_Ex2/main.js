function multiplicaBy()
    {
      let num1 = document.getElementById(
        "N1").value;
      let num2 = document.getElementById(
        "N2").value;
      document.getElementById(
        "Resultado").innerHTML = num1 * num2;
    }

    function divideBy() 
    { 
      let num1 = document.getElementById(
        "N1").value;
      let num2 = document.getElementById(
        "N2").value;
      document.getElementById(
        "Resultado").innerHTML = num1 / num2;
    }